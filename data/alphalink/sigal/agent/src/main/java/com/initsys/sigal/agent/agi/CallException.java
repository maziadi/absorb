package com.initsys.sigal.agent.agi;

/**
 * Thrown when an exception occurs while handling a call.
 */
public class CallException extends RuntimeException {

    /** Serial version UID */
    private static final long serialVersionUID = 7854951067763635494L;

    public CallException(String message, Throwable cause) {
        super(message, cause);
    }

    public CallException(String message) {
        super(message);
    }

}
